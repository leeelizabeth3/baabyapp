export const PCT_MARKS = [3, 15, 50, 85, 97];

// Quarterly chart points: 0,3,6,...,48 (17 points)
export const CHART_MONTHS = Array.from({ length: 17 }, (_, i) => i * 3);

export function nearestChartMonth(m) {
  return CHART_MONTHS.reduce((prev, curr) =>
    Math.abs(curr - m) < Math.abs(prev - m) ? curr : prev
  );
}

export function getPercentile(value, month, sex, table) {
  const data = table[sex];
  // Snap to nearest available data point
  const available = Object.keys(data).map(Number).sort((a, b) => a - b);
  const nearest = available.reduce((prev, curr) =>
    Math.abs(curr - month) < Math.abs(prev - month) ? curr : prev
  );
  const d = data[nearest];
  if (!d) return null;
  if (value <= d[0]) return Math.max(1, Math.round(3 * value / d[0]));
  if (value >= d[4]) return Math.min(99, Math.round(97 + 2 * (value - d[4]) / (d[4] - d[3])));
  for (let i = 0; i < 4; i++) {
    if (value >= d[i] && value <= d[i + 1]) {
      const f = (value - d[i]) / (d[i + 1] - d[i]);
      return Math.round(PCT_MARKS[i] + f * (PCT_MARKS[i + 1] - PCT_MARKS[i]));
    }
  }
  return 50;
}

export function getZone(pct) {
  if (pct < 15) return 'low';
  if (pct > 85) return 'high';
  return 'normal';
}

export function formatAge(months) {
  if (months < 24) return `${months}개월`;
  const y = Math.floor(months / 12);
  const m = months % 12;
  return m > 0 ? `${y}세 ${m}개월` : `${y}세`;
}
