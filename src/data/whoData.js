export const PCT_MARKS = [3, 15, 50, 85, 97];

export function getPercentile(value, month, sex, table) {
  const d = table[sex][month];
  if (!d) return null;
  if (value <= d[0]) return Math.max(1, Math.round(3 * value / d[0]));
  if (value >= d[4]) return Math.min(99, Math.round(97 + 2 * (value - d[4]) / (d[4] - d[3])));
  for (let i = 0; i < 4; i++) {
    if (value >= d[i] && value <= d[i + 1]) {
      const f = (value - d[i]) / (d[i + 1] - d[i]);
      return Math.round(PCT_MARKS[i] + f * (PCT_MARKS[i + 1] - PCT_MARKS[i]));
    }
  }
  return 50;
}

export function getZone(pct) {
  if (pct < 15) return 'low';
  if (pct > 85) return 'high';
  return 'normal';
}
